let box = "Woody";
console.log(box);

const pi = 3.14;
console.log(pi);

var number = 10;
var number = 12;
console.log(number);

let letter = "A";
letter = "B";
console.log(letter);

// 'let' and 'const' have block scope, while 'var' has function scope
// 'var' can be re-declared and reassigned
// 'const' cannot be re-declared nor reassigned
